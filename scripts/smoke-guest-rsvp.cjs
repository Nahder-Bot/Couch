'use strict';

let passed = 0;
let failed = 0;

function eq(label, actual, expected) {
  if (actual === expected) { console.log(`  ok ${label}`); passed++; return; }
  console.error(`  FAIL ${label}`);
  console.error(`     expected: ${JSON.stringify(expected)}`);
  console.error(`     actual:   ${JSON.stringify(actual)}`);
  failed++;
}
function eqObj(label, actual, expected) {
  const a = JSON.stringify(actual);
  const e = JSON.stringify(expected);
  if (a === e) { console.log(`  ok ${label}`); passed++; return; }
  console.error(`  FAIL ${label}`);
  console.error(`     expected: ${e}`);
  console.error(`     actual:   ${a}`);
  failed++;
}

console.log('smoke-guest-rsvp.cjs — Phase 27 helper contracts');
console.log('=================================================');

// --- 1. guestId format ---
//   Generated by: crypto.randomBytes(16).toString('base64url') in queuenight CFs.
//   16 bytes -> 22 base64url characters with no padding.
const GUEST_ID_REGEX = /^[A-Za-z0-9_-]{20,32}$/;
{
  const sample = require('crypto').randomBytes(16).toString('base64url');
  eq('1.1 guestId regex matches base64url(16)', GUEST_ID_REGEX.test(sample), true);
  eq('1.2 guestId regex rejects 8-char string',  GUEST_ID_REGEX.test('abc12345'), false);
  eq('1.3 guestId regex rejects 50-char string', GUEST_ID_REGEX.test('a'.repeat(50)), false);
  eq('1.4 guestId length is 22 for 16 bytes',    sample.length, 22);
}

// --- 2. Name-collision render rule (D-04) ---
//   if familyMemberNames.has(name.trim().toLowerCase()) -> "{name} (guest)"
//   else                                                -> "{name}"
function displayGuestName(rawName, familyMemberNames) {
  const norm = (rawName || '').trim().toLowerCase();
  if (familyMemberNames.has(norm)) return rawName + ' (guest)';
  return rawName;
}
{
  const fam = new Set(['alex', 'sam', 'jamie']);
  eq('2.1 collision: "Alex" -> "Alex (guest)"',     displayGuestName('Alex', fam),     'Alex (guest)');
  eq('2.2 no collision: "Riley" -> "Riley"',        displayGuestName('Riley', fam),    'Riley');
  eq('2.3 case-insensitive: "ALEX" -> "ALEX (guest)"', displayGuestName('ALEX', fam),  'ALEX (guest)');
  eq('2.4 trim + lowercase: "  Sam " -> "  Sam  (guest)"', displayGuestName('  Sam ', fam), '  Sam  (guest)');
}

// --- 3. Response normalization (D-06 reminder cadence) ---
function normalizeResponseForReminders(r) {
  if (r === 'yes')   return 'yes';
  if (r === 'maybe') return 'maybe';
  if (r === 'no')    return 'no';
  return 'notResp';
}
{
  eq('3.1 yes -> yes',          normalizeResponseForReminders('yes'),       'yes');
  eq('3.2 maybe -> maybe',      normalizeResponseForReminders('maybe'),     'maybe');
  eq('3.3 no -> no',            normalizeResponseForReminders('no'),        'no');
  eq('3.4 undefined -> notResp', normalizeResponseForReminders(undefined),  'notResp');
  eq('3.5 null -> notResp',     normalizeResponseForReminders(null),        'notResp');
  eq('3.6 garbage -> notResp',  normalizeResponseForReminders('xyz'),       'notResp');
}

// --- 4. Guest-count helper (D-01 wp.guestCount denorm) ---
function visibleGuestCount(guests) {
  return (Array.isArray(guests) ? guests : []).filter(g => g && !g.revoked).length;
}
{
  const sample = [
    { guestId: 'a', name: 'A', response: 'yes',   revoked: false },
    { guestId: 'b', name: 'B', response: 'maybe', revoked: false },
    { guestId: 'c', name: 'C', response: 'no',    revoked: true  },
    null,
    { guestId: 'd', name: 'D', response: 'yes' } // revoked absent -> truthy by !g.revoked
  ];
  eq('4.1 visibleGuestCount counts non-revoked + skips null', visibleGuestCount(sample), 3);
  eq('4.2 visibleGuestCount empty array -> 0',                visibleGuestCount([]),      0);
  eq('4.3 visibleGuestCount undefined -> 0',                  visibleGuestCount(undefined), 0);
}

// Suppress unused-import warning for eqObj — exposed for Plan 05 sentinel additions.
void eqObj;

